Organ donation is an amazing opportunity. The concept of giving a part of yourself to someone to make them whole and healthy is absolutely awe-inspiring. Given that there are more than 120,000 people on the waiting list currently, with more than 4,300 of those in Illinois alone, the impact a single donor can make is immense. And the best thing a person can do is to spread awareness of the need for, and the ease of registering to be an organ and tissue donor.

Your donation means the world to a person in need, and most likely it will be someone in your immediate area, a person who can directly impact your community. As you can imagine, finding the right match for these donations is no simple task, but as we create more awareness, more organ & tissue recipients will have the chance of getting their lives saved or enhanced by donors.

There are a few things that anyone, registered donor or not, can do to raise awareness for the need for donors:

Update Your Social Media Photos
One way to spread awareness about organ donation is to update your social media profile photos with themed banners. By visiting the Donate Life website, you can find links to banners, cover images, and profile pictures for social media accounts like Facebook and Twitter. This is a great way to show your support for organ and tissue donation. You can, of course, write posts, tag friends who have been affected by the donation process, and share blogs like this and other articles showing just how easy it is to save a life.<br>





